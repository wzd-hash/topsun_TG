package com.topsun.entity;

public class Round {

	private String busID;
	private String plateNO;
	private String direction; //转弯角度
	private String startTime; //开始时间
	private String endTime; //结束时间
	private String speed;  //平均时速
	private String orientation;  //转弯方向
	private String companyName;
	
	private String start_direction;
	private String end_direction;
	
	private String latitude; 
	private String longitude; 
	
	
	private String num;
	
	
	
	
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getStart_direction() {
		return start_direction;
	}
	public void setStart_direction(String start_direction) {
		this.start_direction = start_direction;
	}
	public String getEnd_direction() {
		return end_direction;
	}
	public void setEnd_direction(String end_direction) {
		this.end_direction = end_direction;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getBusID() {
		return busID;
	}
	public void setBusID(String busID) {
		this.busID = busID;
	}
	public String getPlateNO() {
		return plateNO;
	}
	public void setPlateNO(String plateNO) {
		this.plateNO = plateNO;
	}
	public String getDirection() {
		return direction;
	}
	public void setDirection(String direction) {
		this.direction = direction;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getOrientation() {
		return orientation;
	}
	public void setOrientation(String orientation) {
		this.orientation = orientation;
	}
	
	
	
	
}
